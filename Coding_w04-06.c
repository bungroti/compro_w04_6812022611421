#include <stdio.h>
// ประกาศค่าคงที่แบบ global
const int GLOBAL_RATE = 10;
void calculate() {
    // ประกาศค่าคงที่แบบ local
    const int LOCAL_BONUS = 50;
    printf("GLOBAL_RATE = %d\n", GLOBAL_RATE);
    printf("LOCAL_BONUS = %d\n", LOCAL_BONUS);
}

int main() {
    calculate();
    // ใช้งานค่าคงที่ global ได้ใน main
    printf("GLOBAL_RATE in main = %d\n", GLOBAL_RATE);
    // ทดลองใช้ค่าคงที่ local ที่อยู่นอกฟังก์ชัน
    // จะเกิด compile-time error เพราะ LOCAL_BONUS ไม่สามารถเข้าถึงได้จากภายนอกฟังก์ชัน calculate
    // printf("LOCAL_BONUS in main = %d\n", LOCAL_BONUS); // compile-time error
    return 0;
}
/*1.ค่าคงที่ GLOBAL_RATE และ LOCAL_BONUS ต่างกันอย่างไร?
- GLOBAL_RATE คือค่าคงที่ระดับ global ถูกประกาศนอกฟังก์ชัน สามารถใช้ได้ทุกที่ในไฟล์
- LOCAL_BONUS คือค่าคงที่ระดับ local ถูกประกาศในฟังก์ชัน calculate() ใช้ได้เฉพาะภายในฟังก์ชันนั้น
2. เกิดอะไรขึ้นถ้าเราพิมพ์ GLOBAL_RATE = 20; หรือ LOCAL_BONUS = 80;?
- จะเกิด compile-time error เพราะค่าคงที่ (const) ไม่สามารถเปลี่ยนแปลงค่าได้
3. ทำไม LOCAL_BONUS ถึงไม่สามารถแสดงค่าจากใน main() ได้?
- เพราะ LOCAL_BONUS ถูกประกาศในขอบเขตของฟังก์ชัน calculate()
   ซึ่งทำให้มันไม่สามารถเข้าถึงได้จากภายนอกฟังก์ชันนั้น
- เมื่อพยายามใช้ LOCAL_BONUS ใน main() จะเกิด compile-time error
4.อภิปรายผล
- จากการทดลองพบว่า ค่าคงที่ที่ประกาศในระดับ global สามารถเข้าถึงได้จากทุกฟังก์ชัน
  แต่ค่าคงที่ที่ประกาศในระดับ local จะถูกจำกัดการใช้งานเฉพาะภายในฟังก์ชันนั้น ๆ เท่านั้น
- ส่วนค่าคงที่ที่ประกาศในระดับ local จะไม่สามารถเข้าถึงได้จากภายนอกฟังก์ชัน
  ซึ่งช่วยป้องกันการเปลี่ยนแปลงค่าที่ไม่ตั้งใจและทำให้โค้ดมีความชัดเจนมากขึ้น
- การใช้ const ช่วยให้โปรแกรมเมอร์สามารถกำหนดค่าคงที่ที่ไม่ควรเปลี่ยนแปลงได้
  ซึ่งเป็นแนวทางที่ดีในการเขียนโปรแกรมที่ปลอดภัยและมีประสิทธิภาพ
- ความเข้าใจเรื่องขอบเขตของตัวแปร (scope) และค่าคงที่ เป็นพื้นฐานสำคัญในการเขียนโปรแกรม
  โดยเฉพาะเมื่อเขียนโปรแกรมที่มีหลายฟังก์ชันและต้องจัดการข้อมูลร่วมกัน
*/
